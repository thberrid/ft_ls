unsigned int	ft_intlen(int nbr)
{
	unsigned int	len;

	len = 1;
	if (nbr < 0)
	{
		nbr *= -1;
		len += 1;
	}
	while (nbr >= 10)
	{
		nbr /= 10;
		len += 1;
	}
	return (len);
}
